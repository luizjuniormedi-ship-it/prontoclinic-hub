-- Tenant isolation for LIS, NPS, pre-registration and notifications.
-- Existing functional authorization is retained and narrowed to the tenant.
-- Deliberately does not FORCE RLS; owner semantics are validated separately.
BEGIN;

DO $$
DECLARE
  target_table text;
BEGIN
  IF to_regprocedure('public.request_company_id()') IS NULL THEN
    RAISE EXCEPTION 'Operational RLS requires public.request_company_id()';
  END IF;
  IF to_regprocedure('public.current_company_id()') IS NULL THEN
    RAISE EXCEPTION 'Operational RLS requires public.current_company_id()';
  END IF;
  IF NOT EXISTS (SELECT 1 FROM pg_roles WHERE rolname = 'app_prontomedic') THEN
    RAISE EXCEPTION 'Operational RLS requires role app_prontomedic';
  END IF;

  FOREACH target_table IN ARRAY ARRAY[
    'exames_lab_catalogo', 'exames_lab_valor_referencia',
    'exames_lab_pedido', 'exames_lab_pedido_itens',
    'exames_lab_resultado', 'exames_lab_alerta_critico',
    'nps_pesquisas', 'nps_respostas', 'pre_cadastro',
    'notification_templates', 'notifications',
    'notification_preferences', 'notification_logs'
  ] LOOP
    IF to_regclass('public.' || target_table) IS NULL THEN
      RAISE EXCEPTION 'Operational RLS requires public.%', target_table;
    END IF;
    EXECUTE format('ALTER TABLE public.%I ENABLE ROW LEVEL SECURITY', target_table);
  END LOOP;
END
$$;

GRANT SELECT, INSERT, UPDATE, DELETE ON
