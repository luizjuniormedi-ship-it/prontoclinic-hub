CREATE POLICY "pre_cadastro_anon_insert" ON public.pre_cadastro
  FOR INSERT TO anon
  WITH CHECK (
    length(full_name) >= 3
    AND length(cpf) IN (11, 14)
    AND length(email) >= 5
    AND email LIKE '%@%'
  );
