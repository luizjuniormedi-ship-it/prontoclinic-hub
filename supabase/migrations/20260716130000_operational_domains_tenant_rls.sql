-- Remove only the permissive PUBLIC policies generated by the legacy baseline.
-- Their exact names are deterministic; functional named policies are handled below.
DO $$
DECLARE
  permissive_policy record;
BEGIN
  FOR permissive_policy IN
    SELECT schemaname, tablename, policyname
    FROM pg_policies
    WHERE schemaname = 'public'
      AND tablename = ANY (ARRAY[
        'exames_lab_catalogo', 'exames_lab_valor_referencia',
        'exames_lab_pedido', 'exames_lab_pedido_itens',
        'exames_lab_resultado', 'exames_lab_alerta_critico',
        'nps_pesquisas', 'nps_respostas', 'pre_cadastro',
        'notification_templates', 'notifications',
        'notification_preferences', 'notification_logs'
      ])
      AND policyname = tablename || '_' || lower(cmd)
      AND 'public' = ANY (roles)
  LOOP
    EXECUTE format(
      'DROP POLICY %I ON %I.%I',
      permissive_policy.policyname,
      permissive_policy.schemaname,
      permissive_policy.tablename
    );
  END LOOP;
END
$$;

