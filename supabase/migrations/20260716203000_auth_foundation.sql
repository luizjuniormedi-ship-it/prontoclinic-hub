-- Em instalações legadas, uma matriz sem empresa só pode ser migrada
-- automaticamente quando existe uma única empresa. Com múltiplas empresas,
-- abortamos em vez de atribuir permissões ao tenant errado.
DO $$
DECLARE
  v_company_count INTEGER;
  v_company_id UUID;
BEGIN
  IF EXISTS (SELECT 1 FROM public.role_permissions WHERE company_id IS NULL) THEN
    SELECT COUNT(*), MIN(id) INTO v_company_count, v_company_id
    FROM public.companies;

    IF v_company_count <> 1 THEN
      RAISE EXCEPTION
        'AUTH_FOUNDATION_PREFLIGHT: role_permissions sem company_id e % empresas; mapeamento manual obrigatório',
        v_company_count;
    END IF;

    UPDATE public.role_permissions
       SET company_id = v_company_id
     WHERE company_id IS NULL;
  END IF;
END;
$$;

ALTER TABLE public.role_permissions
  ALTER COLUMN company_id SET NOT NULL,
  ALTER COLUMN role_id SET NOT NULL,
  ALTER COLUMN module SET NOT NULL;

DO $$
DECLARE
  v_constraint RECORD;
BEGIN
  -- Remove apenas uniques legadas exatamente sobre (role_id,module).
  FOR v_constraint IN
    SELECT c.conname
    FROM pg_constraint c
    WHERE c.conrelid = 'public.role_permissions'::regclass
      AND c.contype = 'u'
      AND (
        SELECT array_agg(a.attname ORDER BY key.ordinality)
        FROM unnest(c.conkey) WITH ORDINALITY AS key(attnum, ordinality)
        JOIN pg_attribute a
          ON a.attrelid = c.conrelid
         AND a.attnum = key.attnum
      ) = ARRAY['role_id', 'module']::name[]
  LOOP
    EXECUTE format('ALTER TABLE public.role_permissions DROP CONSTRAINT %I', v_constraint.conname);
  END LOOP;
END;
$$;

CREATE UNIQUE INDEX IF NOT EXISTS role_permissions_company_role_module_idx
  ON public.role_permissions (company_id, role_id, module);

DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint
    WHERE conrelid = 'public.role_permissions'::regclass
      AND contype = 'f'
      AND confrelid = 'public.companies'::regclass
  ) THEN
    ALTER TABLE public.role_permissions
      ADD CONSTRAINT role_permissions_company_id_fkey
      FOREIGN KEY (company_id) REFERENCES public.companies(id) ON DELETE CASCADE;
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint
    WHERE conrelid = 'public.role_permissions'::regclass
      AND contype = 'f'
      AND confrelid = 'public.roles'::regclass
  ) THEN
    ALTER TABLE public.role_permissions
      ADD CONSTRAINT role_permissions_role_id_fkey
      FOREIGN KEY (role_id) REFERENCES public.roles(id) ON DELETE CASCADE;
  END IF;
END;
$$;

-- ---------------------------------------------------------------------------
-- Identidade canônica: public.user_profiles.id = auth.users.id
-- ---------------------------------------------------------------------------
UPDATE public.user_profiles
   SET user_id = id
 WHERE user_id IS NULL;

DO $$
BEGIN
  IF EXISTS (
    SELECT 1 FROM public.user_profiles
    WHERE user_id IS DISTINCT FROM id
  ) THEN
    RAISE EXCEPTION
      'AUTH_FOUNDATION_PREFLIGHT: user_profiles.id e user_id divergentes; reconciliação manual obrigatória';
  END IF;

  IF EXISTS (
    SELECT 1
    FROM public.user_profiles up
    LEFT JOIN auth.users au ON au.id = up.id
    WHERE au.id IS NULL
  ) THEN
    RAISE EXCEPTION
      'AUTH_FOUNDATION_PREFLIGHT: existem user_profiles órfãos sem auth.users correspondente';
  END IF;

  IF EXISTS (
    SELECT 1
    FROM public.user_profiles up
    LEFT JOIN public.roles r ON r.id = up.role_id
    WHERE up.role_id IS NOT NULL AND r.id IS NULL
  ) THEN
    RAISE EXCEPTION
      'AUTH_FOUNDATION_PREFLIGHT: existem role_id sem papel correspondente';
  END IF;
END;
$$;

UPDATE public.user_profiles up
   SET role_id = r.id,
       role_name = r.name
  FROM public.roles r
 WHERE up.role_id IS NULL
   AND CASE lower(COALESCE(up.role_name, ''))
         WHEN 'médico' THEN 'medico'
         WHEN 'recepção' THEN 'recepcao'
         WHEN 'enfermeiro' THEN 'enfermagem'
         ELSE lower(COALESCE(up.role_name, ''))
       END = r.name;

DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint
    WHERE conrelid = 'public.user_profiles'::regclass
      AND contype = 'f'
      AND confrelid = 'auth.users'::regclass
      AND conkey = ARRAY[(SELECT attnum FROM pg_attribute
                          WHERE attrelid = 'public.user_profiles'::regclass
                            AND attname = 'id')]::smallint[]
  ) THEN
    ALTER TABLE public.user_profiles
      ADD CONSTRAINT user_profiles_id_auth_users_fkey
      FOREIGN KEY (id) REFERENCES auth.users(id) ON DELETE CASCADE;
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint
    WHERE conrelid = 'public.user_profiles'::regclass
      AND contype = 'f'
      AND confrelid = 'public.roles'::regclass
      AND conkey = ARRAY[(SELECT attnum FROM pg_attribute
                          WHERE attrelid = 'public.user_profiles'::regclass
                            AND attname = 'role_id')]::smallint[]
  ) THEN
    ALTER TABLE public.user_profiles
      ADD CONSTRAINT user_profiles_role_id_fkey
      FOREIGN KEY (role_id) REFERENCES public.roles(id);
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint
    WHERE conrelid = 'public.user_profiles'::regclass
      AND conname = 'user_profiles_identity_consistent'
  ) THEN
    ALTER TABLE public.user_profiles
      ADD CONSTRAINT user_profiles_identity_consistent
      CHECK (user_id = id);
  END IF;
END;
$$;

CREATE OR REPLACE FUNCTION public.sync_user_profile_role()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public, pg_temp
AS $$
DECLARE
  v_role public.roles%ROWTYPE;
BEGIN
  IF NEW.role_name IS NOT NULL
     AND (TG_OP = 'INSERT' OR NEW.role_name IS DISTINCT FROM OLD.role_name) THEN
    SELECT * INTO v_role
    FROM public.roles
    WHERE name = CASE lower(NEW.role_name)
      WHEN 'médico' THEN 'medico'
      WHEN 'recepção' THEN 'recepcao'
      WHEN 'enfermeiro' THEN 'enfermagem'
      ELSE lower(NEW.role_name)
    END
      AND lg_ativo = TRUE;

    IF NOT FOUND THEN
      RAISE EXCEPTION 'Perfil de acesso inválido: %', NEW.role_name;
    END IF;

    NEW.role_id := v_role.id;
    NEW.role_name := v_role.name;
  ELSIF NEW.role_id IS NOT NULL
        AND (TG_OP = 'INSERT' OR NEW.role_id IS DISTINCT FROM OLD.role_id) THEN
    SELECT * INTO v_role
    FROM public.roles
    WHERE id = NEW.role_id AND lg_ativo = TRUE;

    IF NOT FOUND THEN
      RAISE EXCEPTION 'Perfil de acesso inválido: %', NEW.role_id;
    END IF;

    NEW.role_name := v_role.name;
  END IF;

  NEW.user_id := NEW.id;
  NEW.updated_at := NOW();
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_sync_user_profile_role ON public.user_profiles;
CREATE TRIGGER trg_sync_user_profile_role
BEFORE INSERT OR UPDATE OF id, user_id, role_id, role_name
ON public.user_profiles
FOR EACH ROW EXECUTE FUNCTION public.sync_user_profile_role();

