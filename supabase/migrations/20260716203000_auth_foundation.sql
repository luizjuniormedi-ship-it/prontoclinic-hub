-- ---------------------------------------------------------------------------
-- Auditoria: retornar o id persistido e preservar RLS nas estatísticas
-- ---------------------------------------------------------------------------
DROP FUNCTION IF EXISTS public.log_data_access(TEXT, TEXT, TEXT, JSONB);
CREATE FUNCTION public.log_data_access(
  p_tabela TEXT,
  p_registro_id TEXT,
  p_acao TEXT,
  p_contexto JSONB DEFAULT '{}'::JSONB
)
RETURNS BIGINT
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public, pg_temp
AS $$
DECLARE
  v_log_id BIGINT;
  v_company_id UUID;
  v_user_id UUID;
  v_user_name TEXT;
  v_role TEXT;
BEGIN
  v_user_id := auth.uid();

  IF v_user_id IS NULL THEN
    RAISE EXCEPTION 'Não autenticado';
  END IF;

  SELECT company_id, full_name, role_name
    INTO v_company_id, v_user_name, v_role
    FROM public.user_profiles
   WHERE id = v_user_id AND lg_ativo = TRUE;

  IF NOT FOUND OR v_company_id IS NULL THEN
    RAISE EXCEPTION 'Perfil ativo e empresa são obrigatórios';
  END IF;

  INSERT INTO public.audit_logs (
    company_id,
    cd_usuario, cd_usuario_nome, role_name,
    acao, tabela, registro_id, operacao,
    dados_novos
  ) VALUES (
    v_company_id,
    v_user_id, v_user_name, v_role,
    p_acao, p_tabela, p_registro_id,
    p_tabela || ' ' || p_acao || ' via API',
    p_contexto
  )
  RETURNING id INTO v_log_id;

  RETURN v_log_id;
END;
$$;

REVOKE ALL ON FUNCTION public.log_data_access(TEXT, TEXT, TEXT, JSONB) FROM PUBLIC;
GRANT EXECUTE ON FUNCTION public.log_data_access(TEXT, TEXT, TEXT, JSONB) TO authenticated;

DROP VIEW IF EXISTS public.audit_logs_stats;
CREATE VIEW public.audit_logs_stats
WITH (security_invoker = true)
AS
SELECT
  company_id,
  DATE_TRUNC('day', dt_evento) AS dia,
  acao,
  tabela,
  cd_usuario,
  cd_usuario_nome,
  COUNT(*) AS total
FROM public.audit_logs
GROUP BY company_id, DATE_TRUNC('day', dt_evento), acao, tabela, cd_usuario, cd_usuario_nome;

GRANT SELECT ON public.audit_logs TO authenticated;
GRANT SELECT ON public.audit_logs_stats TO authenticated;
