CREATE OR REPLACE FUNCTION public.auth_profile_company_id()
RETURNS UUID
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public, pg_temp
SET row_security = off
AS $$
  SELECT company_id
  FROM public.user_profiles
  WHERE id = auth.uid() AND lg_ativo = TRUE;
$$;

CREATE OR REPLACE FUNCTION public.auth_is_company_admin()
RETURNS BOOLEAN
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public, pg_temp
SET row_security = off
AS $$
  SELECT COALESCE(EXISTS (
    SELECT 1
    FROM public.user_profiles up
    LEFT JOIN public.roles r ON r.id = up.role_id
    WHERE up.id = auth.uid()
      AND up.lg_ativo = TRUE
      AND COALESCE(r.name, lower(up.role_name)) IN ('admin', 'administrador')
  ), FALSE);
$$;

REVOKE ALL ON FUNCTION public.auth_profile_company_id() FROM PUBLIC;
REVOKE ALL ON FUNCTION public.auth_is_company_admin() FROM PUBLIC;
GRANT EXECUTE ON FUNCTION public.auth_profile_company_id() TO authenticated;
GRANT EXECUTE ON FUNCTION public.auth_is_company_admin() TO authenticated;

DO $$
DECLARE
  v_unknown_policies TEXT[];
BEGIN
  SELECT array_agg(format('%I.%I', tablename, policyname) ORDER BY tablename, policyname)
    INTO v_unknown_policies
  FROM pg_policies
  WHERE schemaname = 'public'
    AND tablename IN ('user_profiles', 'roles', 'role_permissions')
    AND policyname NOT IN (
      'user_profiles_select_authorized',
      'user_profiles_insert_company_admin',
      'user_profiles_update_company_admin',
      'roles_select_authenticated',
      'role_permissions_select_company',
      'role_permissions_insert_company_admin',
      'role_permissions_update_company_admin'
    );

  IF v_unknown_policies IS NOT NULL THEN
    RAISE EXCEPTION
      'AUTH_FOUNDATION_PREFLIGHT: policies legadas/desconhecidas exigem reconciliação manual: %',
      v_unknown_policies;
  END IF;
END;
$$;

