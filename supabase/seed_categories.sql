CREATE TABLE IF NOT EXISTS public.service_categories (
  id SERIAL PRIMARY KEY,
  company_id UUID REFERENCES public.companies(id),
  codigo VARCHAR(10) UNIQUE, nome VARCHAR(100) NOT NULL,
  parent_id INTEGER REFERENCES public.service_categories(id),
  lg_cbhpm BOOLEAN DEFAULT FALSE, cd_origem_sigh INTEGER,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

ALTER TABLE public.service_categories ENABLE ROW LEVEL SECURITY;
DROP POLICY IF EXISTS "Anyone can read categories" ON public.service_categories;
DROP POLICY IF EXISTS "Admins can manage categories" ON public.service_categories;
CREATE POLICY "Anyone can read categories" ON public.service_categories FOR SELECT TO authenticated USING (true);
CREATE POLICY "Admins can manage categories" ON public.service_categories FOR ALL TO authenticated USING (
  EXISTS (SELECT 1 FROM public.user_profiles WHERE id = auth.uid() AND role_name = 'admin')
);

